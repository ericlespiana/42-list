#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	c_nb[20];
	int		i;

	i = 0;
	if (nb == 0)
		write(1, "0", 1);
	else if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else
	{
		if (nb < 0)
		{
			write(1, "-", 1);
			nb = -nb;
		}
		while (nb)
		{
			c_nb[i++] = nb % 10 + '0';
			nb /= 10;
		}
		while (i > 0)
			write(1, &c_nb[--i], 1);
	}
}
